void vesa_scrivi_stringa(char *src, unsigned int RGB, unsigned int x, unsigned int y)
{
	char *tmp = src;
	unsigned int cx = x;
	unsigned int i = 0;
	
	while(tmp[i] != '\0')
	{
		vesa_put_char(tmp[i++], RGB, cx, y);
		cx += FONT_WIDTH;
	}
}


void vesa_put_char(char ascii, unsigned int RGB, unsigned int x, unsigned int y)
{
	char *src;
	unsigned int i, j;
	
	// Prende il valore corrispondente al carattere ascii dalla matrice
	src = default_font_8x16[ascii];
		
	for(i = 0; i < FONT_HEIGHT; i++)
	{
		for(j = 0; j < FONT_WIDTH; j++)
		{			
			if((src[i] & (1 << j)) > 0)	// Se il pixel è contrassegnato come presente
				vesa_draw_pixel(x+j, y+i-4, (unsigned int) RGB);	// Lo disegno
		}
	}
}

