#include <driver/video/vesa.h>

// Inizializza la modalità vesa
void vesa_init(void *vbe_control_info, void *vbe_mode_info)
{
	vci = (vesa_info_t *) vbe_control_info;
	vmi = (vesa_mode_info_t *) vbe_mode_info;
	vesamem = (char *)vmi->phys_base_ptr;
	vesasize = (vmi->bytes_per_scan_line * vmi->height);
	IsvesaOn = 1;
}

// Disegna un pixel sullo schermo alle coordinate (x,y)
void vesa_draw_pixel(unsigned int x, unsigned int y, unsigned int RGB)
{
	register char *temp;
	
	if (x < 0 || x > vmi->width || y < 0 || y > vmi->height) return;
	
	x = (x * (vmi->bits_per_pixel >> 3));
	y = (y * vmi->bytes_per_scan_line);
	
	temp = &vesamem[x + y];

	temp[0] = RGB & 0xff;
	temp[1] = (RGB>>8) & 0xff;
	temp[2] = (RGB>>16) & 0xff;
}


